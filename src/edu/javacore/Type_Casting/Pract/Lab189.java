package edu.javacore.Type_Casting.Pract;

public class Lab189 {
	public static void main(String[] args) {
		byte b = 10;
		short s = b;
		int a = s;
		long x = a;
		float f = x;
		double d = f;
		System.out.println(b);
		System.out.println(s);
		System.out.println(a);
		System.out.println(x);
		System.out.println(f);
		System.out.println(d);
	}
}
